%%VALORES DE X
x=[100 1000 5000 10000 50000 100000 200000 400000 600000 800000 1000000 2000000 3000000 4000000 5000000 6000000 7000000 8000000 9000000 10000000];
 
%%VALORES DE Y = F(x)
y=[0.0000100136 0.000138998 0.0009970665 0.0022420883 0.0146069527 0.0310289860 0.0672500134 0.1548278332 0.2459578514 0.3538069725 0.4147429466 0.9554150105 1.5406818390 2.1173849106 2.7731540203 3.3597409725 3.9738249779 4.7150800228 5.2244870663 6.3032050133];

%%OBTENER UN POLINOMIO QUE SE AJUSTE A LOS PUNTOS X Y Y
n=3; 				%%GRADO DEL POLINOMIO DE AJUSTE
p=polyfit(x,y,n)	%%FUNCION QUE REALIZA EL AJUSTE POLINOMIAL

%%SE CREA UN ESPACIO PARA REALIZAR UNA GRAFICA EN 
%%DONDE SE COMPARA LA CURVA AJUSTADA CONTRA LOS 
%%VALORES EXPERIMENTALES
xi=linspace(0.72,1.45,1000);	%%ESPACIO DE PUNTOS PARA Xi
z=polyval(p,xi);				%%EVALUACION DEL POLINOMIO P EN EL ESPACIO Xi

%%SE REALIZA LA FIGURA CORRESPONDIENTE
figure(1);
plot(x,y,'go',xi,z,'-'),grid;
ylabel('Segundos');
xlabel('Numeros a ordenar');
title('Ajuste de minimos cuadrados (Shell)');
